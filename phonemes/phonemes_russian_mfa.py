"""
phonemes_russian_mfa.py  ─  IPA → Коды LIP Fallout 2
                             Для модели MFA: russian_mfa

Основано на таблице фонем LIP Fallout 2 от Anchorite и Black_Electric.
Сопоставляет символы IPA русского языка с 41 кодом формы рта.
Маркеры ударения (ˈ, ˌ) и долготы (ː) из MFA удаляются перед поиском.
"""

PHONEME_TABLE: dict[str, int] = {
    # ── Гласные ────────────────────────────────────────────────────────────────
    "a":   0x06,  # а (арбуз)
    "i":   0x01,  # и (ива)
    "u":   0x0A,  # у (утка)
    "e":   0x04,  # э (это)
    "o":   0x08,  # о (окно)
    "ɨ":   0x09,  # ы (мы)

    # ── Взрывные (Оклюзивные) ──────────────────────────────────────────────────
    "p":   0x11,  # п (папа)
    "b":   0x12,  # б (баба)
    "t":   0x13,  # т (тот)
    "d":   0x14,  # д (дом)
    "k":   0x15,  # к (кот)
    "ɡ":   0x16,  # г (год)
    "g":   0x16,  # вариант ASCII

    # ── Фрикативные ───────────────────────────────────────────────────────────
    "f":   0x17,  # ф (фаза)
    "v":   0x18,  # в (вода)
    "s":   0x1B,  # с (сон)
    "z":   0x1C,  # з (зуб)
    "ʃ":   0x1D,  # ш (шаг)
    "ʒ":   0x1E,  # ж (жар)
    "x":   0x1F,  # х (хор)
    "h":   0x1F,  # вариант (аспирация)

    # ── Носовые ────────────────────────────────────────────────────────────────
    "m":   0x20,  # м (мама)
    "n":   0x21,  # н (нос)
    "ŋ":   0x22,  # н (перед к/г, напр. "банк")

    # ── Аппроксиманты и плавные ────────────────────────────────────────────────
    "l":   0x23,  # л (лампа)
    "w":   0x24,  # (редко в заимствованиях)
    "j":   0x25,  # й (йод)
    "r":   0x26,  # р (рыба)

    # ── Аффрикаты ─────────────────────────────────────────────────────────────
    "tʃ":  0x27,  # ч (час)
    "t͡s":  0x27,  # ц (цапля) - часто сопоставляется с 'ch'

    # ── Тишина / неречевые звуки ───────────────────────────────────────────────
    "sil": 0x00,  # тишина
    "sp":  0x00,  # пауза
    "spn": 0x00,  # речевой шум
    "":    0x00,
}
